-- TABLES BONNOTE --

CREATE TABLE IF NOT EXISTS INSCRIT (
	INE VARCHAR(40) PRIMARY KEY,
	Pseudo VARCHAR(40),
	MotDePasse VARCHAR(40)		
	); 

CREATE TABLE IF NOT EXISTS OFFRE (
	id_offre INTEGER PRIMARY KEY,
	NomOffre VARCHAR(40),
	Prix INTEGER
	); 
	
CREATE TABLE IF NOT EXISTS COMMANDE (
	id_commande INTEGER PRIMARY KEY NOT NULL AUTO_INCREMENT,
	INE VARCHAR(40),
	FOREIGN KEY (INE) REFERENCES INSCRIT(INE),	
	NomMatiere VARCHAR(40),
	id_offre INTEGER,
	FOREIGN KEY (id_offre) REFERENCES OFFRE(id_offre)
	); 

